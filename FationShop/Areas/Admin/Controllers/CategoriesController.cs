using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.IO;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using FationShop.Areas.Admin.Framework;
using PagedList;
using PagedList.Mvc;

namespace FationShop.Areas.Admin.Controllers
{
    [Authorize]
    public class CategoriesController : Controller
    {
        private FashionShopEntities db = new FashionShopEntities();

        // GET: Admin/Categories
        public ActionResult Index(string searchString, int? page, int pageLimit = 10)
        {
            //Lưu search string vào viewbag
            ViewBag.searchString = searchString;
            return View(db.Categories.Where(x => x.Name.StartsWith(searchString) || searchString == null).ToList().ToPagedList(page ?? 1, pageLimit));
        }

        // GET: Admin/Categories/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Category category = db.Categories.Find(id);
            if (category == null)
            {
                return HttpNotFound();
            }
            return View(category);
        }

        // GET: Admin/Categories/Create
        public ActionResult Create()
        {
            return View();
        }

        // POST: Admin/Categories/Create
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create(Category category)
        {
            string fileName = Path.GetFileNameWithoutExtension(category.ImageFile1.FileName);
            string extension = Path.GetExtension(category.ImageFile1.FileName);
            fileName = fileName + DateTime.Now.ToString("yymmssfff") + extension;
            category.Image = "/Areas/Admin/Image/" + fileName;
            fileName = Path.Combine(Server.MapPath("~/Areas/Admin/Image/"), fileName);
            category.ImageFile1.SaveAs(fileName);
            db.Categories.Add(category);
            db.SaveChanges();
            return RedirectToAction("Index");

        }

        // GET: Admin/Categories/Edit/5
        public ActionResult Edit(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Category category = db.Categories.Find(id);
            if (category == null)
            {
                return HttpNotFound();
            }
            return View(category);
        }

        // POST: Admin/Categories/Edit/5
        // To protect from overposting attacks, enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "ID,Name,MetaTitle,DisplayOrder,Status")] Category category)
        {
            if (ModelState.IsValid)
            {
                db.Entry(category).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(category);
        }

        // GET: Admin/Categories/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Category category = db.Categories.Find(id);
            if (category == null)
            {
                return HttpNotFound();
            }
            return View(category);
        }

        // POST: Admin/Categories/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Category category = db.Categories.Find(id);
            db.Categories.Remove(category);
            db.SaveChanges();
            return RedirectToAction("Index");
        }

        protected override void Dispose(bool disposing)
        {
            if (disposing)
            {
                db.Dispose();
            }
            base.Dispose(disposing);
        }
    }
}
